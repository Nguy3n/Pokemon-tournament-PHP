      <p>Pokémonturnering - eventside</p>
Lag en database for en pokémonturnering. Siden er ment for at admin skal
kunne administrere turneringen, og for at folk kan se de planlagte kampene.<br/><br/>

Hver trener kan ha opp til tre pokémon. En kamp er mellom to trenere, og
den som vinner går videre til neste runde. Hver trener har opp til tre Pokémon
de kan bruke i en forhåndsbestemt rekkefølge. I denne oppgaven skal du
fokusere på å lage en oversikt for de første rundene.<br/><br/>

Systemet lar trenere registrere seg selv med sine pokémon, og informasjon
om hver pokémon. Administratorene kan så registrere konkurrenten til en
kamp.<br/><br/>

Administratorer kan bruke systemet til å registrere arenaer(stages) og
kamper. En arena er stedet der kampen foregår og disse kan ha ulike
elementtyper, for eksempel vann.<br/><br/>

Alle brukere kan bruke siden for å se en oversikt over de planlagte kampene.<br/><br/>

Ekstra utfordringer for videre læring:
Trenere kan bruke siden til å laste opp bilder av seg selv og sine pokémon.
Admin kan laste opp bilde av arenaene. I oversikten skal kampene vises med
bilde av arenaen, konkurrentene og deres pokémon.<br/><br/>

